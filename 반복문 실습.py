# for i in range
for i in range(10): # 0 ~ 9까지
    print(i)

print("")

for i in range(5,8): # 5 ~ 7까지
    print(i)

print("")

for i in range(1,10,3): # 1 ~ 10까지 3씩 증가하면서
    print(i)

print("")

for i in [1,2,3,4,5]: # 리스트의 갯수만큼 반복
    print(i)

print("")

for i in list(range(5)): # list(range(n)) => [0,1, ... ,n-2,n-1]
    print(i)

print("")

_list = [3,6,9]
for i in _list: # 리스트 변수도 사용가능
    print(i)

print("")

_str = "정동희"
for i in _str: # 문자열도 가능
    print(i)

print("")
